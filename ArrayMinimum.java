public class ArrayMinimum {

    public static int findMin(int[] array) {
        int min = array[0];
        for (int i = 1; i < array.length; i++) {
            if (array[i] < min) {
                min = array[i];
            }
        }
        return min;
    }

    public static void main(String[] args) {
        int[] array = {5, 2, 8, 1, 3};
        System.out.println("Minimum value: " + findMin(array));
    }
}

